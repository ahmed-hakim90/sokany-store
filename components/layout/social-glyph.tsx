export function SocialGlyph({
  socialKey,
  className,
}: {
  socialKey: string;
  className?: string;
}) {
  const k = socialKey.toLowerCase();
  if (k === "facebook") return <FacebookGlyph className={className} />;
  if (k === "instagram") return <InstagramGlyph className={className} />;
  if (k === "youtube") return <YoutubeGlyph className={className} />;
  if (k === "whatsapp") return <WhatsappGlyph className={className} />;
  if (k === "tiktok") return <TiktokGlyph className={className} />;
  if (k === "x" || k === "twitter") return <XGlyph className={className} />;
  if (k === "linkedin") return <LinkedinGlyph className={className} />;
  if (k === "telegram") return <TelegramGlyph className={className} />;
  if (k === "snapchat") return <SnapchatGlyph className={className} />;
  if (k === "pinterest") return <PinterestGlyph className={className} />;
  return <ExternalGlyph className={className} />;
}

